//-----------------------------------------------------FUNCAO DE FITNESS----------------------------------------------------------------------


int fitness(int *vetor)
{
	int nota_elemento,nota_total;
	nota_total = 0;
	int i,j;
	for(i=0;i<T;i++)
	{
		nota_elemento = (T-1)-i;
		for(j=i+1;j<T;j++)
		{
			if(vetor[i]==vetor[j])
			{
				nota_elemento--;
			}
		}
		for(j=i+1;j<T;j++)
		{
			if(vetor[i] == vetor[j]+(j-i) || vetor[i] == vetor[j]-(j-i))
			{
				nota_elemento--;
			}
		}
		nota_total+=nota_elemento;
	}
	return(nota_total);
}


